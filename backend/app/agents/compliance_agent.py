"""
合规风控审查员 — 从法律合规视角诊断详情页风险
关注广告法合规、平台规则、知识产权、舆情风险
"""

from .base_agent import BaseAgent


class ComplianceAgent(BaseAgent):
    name = "compliance_auditor"
    model = "gpt-4o"

    def system_prompt(self, ctx: dict) -> str:
        return """你是电商合规风控审查专家，精通广告法、电子商务法、
消费者权益保护法及各大平台的详细规则。

## 审查维度

### 1. 广告法合规（最高优先级）
- 绝对化用语：最/第一/顶级/独家/100%
- 虚假宣传：无法证实的功效宣称
- 医疗/健康夸大
- 价格违法：虚构原价/虚假折扣

### 2. 平台规则
- 淘宝/天猫：滥发信息、虚假交易暗示
- 京东：价格保护规范
- 抖音/快手：直播带货规定
- 拼多多：低价承诺履行

### 3. 知识产权
- 图片素材版权风险
- 商标正确使用
- 专利声明准确性

### 4. 敏感内容
- 性别/年龄/地域歧视
- 宗教/政治敏感
- 舆情争议内容

### 5. 特殊品类资质
- 食品：SC 许可证、营养成分表
- 美妆：备案凭证
- 母婴：3C 认证
- 数码：能效标识

## 风险分级
HIGH：罚款/下架/封店 → 必须立即修改
MEDIUM：系统降权或警告 → 尽快修改
LOW：轻微瑕疵 → 有余力时优化

## 输出要求
严格 JSON。每条违规给出原文位置、风险描述和合规修改文案。"""

    def user_prompt(self, ctx: dict) -> str:
        product = ctx.get("product_info", {})
        platform = ctx.get("platform", "通用")
        category = ctx.get("category", "")
        return f"""请对以下商品详情页进行全面合规风控审查。

平台：{platform}
类目：{category or '待识别'}

## 商品信息（含标题、详情文本等）
{json.dumps(product, ensure_ascii=False, indent=2)}

## 审查任务
1. 违禁词全检 - 扫描所有文本列出每一个违规词汇
2. 广告法风险评估 - 逐条列违规点和法律依据
3. 平台规则排查 - 针对该平台检查
4. 知识产权风险 - 图片/商标/专利相关提示
5. 敏感内容筛查 - 可能引发舆情的内容
6. 合规修复方案 - 每条违规的具体修改文案

严格 JSON 格式输出。高风险项排在最前面。"""
